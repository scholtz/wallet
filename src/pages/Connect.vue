<template>
  <PublicLayout>
    <div class="container-fluid">
      <h1>{{ $t("connect.title") }}</h1>

      <div v-if="checkNetwork()">
        {{ $t("swap.network") }}: {{ checkNetwork() }}
      </div>
      <div v-else class="alert alert-danger">
        {{ $t("connect.network_not_supported") }}
      </div>
      <div>
        <h2>{{ $t("connect.address") }}</h2>
        <select v-model="addr" class="form-control">
          <option
            v-for="option in $store.state.wallet.privateAccounts"
            :key="option.addr"
            :value="option.addr"
          >
            {{ option.name + " - " + option.addr }}
          </option>
        </select>
      </div>
      <div>
        <h2>{{ $t("connect.authaddr") }}</h2>
        <select v-model="auth" class="form-control" :disabled="loadingAuth">
          <option
            v-for="option in $store.state.wallet.privateAccounts"
            :key="option.addr"
            :value="option.addr"
          >
            {{ option.name + " - " + option.addr }}
          </option>
        </select>
      </div>
      <div>
        <h2>{{ $t("connect.uri") }}</h2>
        <input
          id="uri"
          v-model="uri"
          type="text"
          class="form-control"
          autocomplete="off"
        />
        <div>
          <button
            class="btn btn-primary m-1"
            :disabled="uri && !connectable"
            @click="clickConnect(uri)"
          >
            {{ $t("connect.connect") }}
          </button>
          or
          <button
            class="btn btn-primary m-1"
            :disabled="!connectable"
            @click="clickPaste"
          >
            {{ $t("connect.clipboard") }}
          </button>
        </div>
        <div>
          <h2>{{ $t("connect.sessions") }}</h2>
          <DataTable
            :value="$store.state.wc.connectors"
            responsive-layout="scroll"
            selection-mode="single"
            :paginator="true"
            :rows="20"
          >
            <Column
              field="id"
              :header="$t('connect.client_id')"
              :sortable="true"
            />
            <Column
              field="address"
              :header="$t('connect.address')"
              :sortable="true"
            >
              <template #body="slotProps">
                {{ slotProps.data.address }}
                <p
                  v-if="
                    slotProps.data.auth &&
                    slotProps.data.auth != slotProps.data.address
                  "
                >
                  (authorized address: {{ slotProps.data.auth }})
                </p>
              </template>
            </Column>
            <Column :header="$t('connect.peer')">
              <template #body="slotProps">
                <div v-if="slotProps.data.peer">
                  <img
                    v-if="
                      slotProps.data.peer.icons &&
                      slotProps.data.peer.icons.length
                    "
                    :src="slotProps.data.peer.icons[0]"
                    width="24"
                    height="24"
                  />
                  <a
                    target="_blank"
                    class="m-1"
                    :href="slotProps.data.peer.url"
                    :title="slotProps.data.peer.description"
                  >
                    {{ slotProps.data.peer.name }}
                  </a>
                </div>
              </template>
            </Column>
            <Column :header="$t('connect.connected')" :sortable="true">
              <template #body="slotProps">
                <input
                  class="form-check-input me-1"
                  type="checkbox"
                  :checked="slotProps.data.connected"
                  disabled
                />
                <button
                  class="btn btn-primary m-1"
                  @click="clickDisconnect(slotProps.data.id)"
                >
                  {{ $t("connect.disconnect") }}
                </button>
              </template>
            </Column>
          </DataTable>
          <h2 id="requests">
            {{ $t("connect.requests") }}
          </h2>
          <DataTable
            v-model:expandedRows="expandedRequests"
            v-model:selection="selectedRequest"
            :value="requests"
            responsive-layout="scroll"
            selection-mode="single"
            :paginator="true"
            :rows="20"
          >
            <Column expander style="width: 5rem" />
            <Column
              field="id"
              :header="$t('connect.request_id')"
              :sortable="true"
            />
            <Column
              field="method"
              :header="$t('connect.method')"
              :sortable="true"
            />
            <Column :header="$t('connect.total_fee')">
              <template #body="slotProps">
                {{ slotProps.data.fee }}
              </template>
            </Column>
            <Column>
              <template #body="slotProps">
                <button
                  class="btn btn-primary m-1"
                  :disabled="!$store.state.wallet.isOpen"
                  @click="clickAccept(slotProps.data.id)"
                >
                  {{ $t("connect.accept") }}
                </button>
                <button
                  class="btn btn-primary m-1"
                  :disabled="!$store.state.wallet.isOpen"
                  @click="clickReject(slotProps.data.id)"
                >
                  Reject
                </button>
              </template>
            </Column>
            <template #expansion="slotProps">
              <div class="p-3">
                <DataTable
                  v-model:expandedRows="expandedTransactions"
                  v-model:selection="selectedTransaction"
                  :value="slotProps.data.transactions"
                  selection-mode="single"
                >
                  <Column expander style="width: 5rem" />
                  <Column
                    field="index"
                    :header="$t('connect.index')"
                    :sortable="true"
                  />
                  <Column
                    field="type"
                    :header="$t('connect.type')"
                    :sortable="true"
                  />
                  <Column
                    field="from"
                    :header="$t('connect.from')"
                    :sortable="true"
                  />
                  <Column
                    field="asset"
                    :header="$t('connect.asset')"
                    :sortable="true"
                  />
                  <Column
                    field="amount"
                    :header="$t('connect.amount')"
                    :sortable="true"
                  />
                  <Column
                    field="fee"
                    :header="$t('connect.fee')"
                    :sortable="true"
                  />
                  <Column
                    field="rekeyTo"
                    :header="$t('connect.rekeyto')"
                    :sortable="true"
                  />
                  <template #expansion="txProps">
                    <div class="p-3">
                      <p>
                        Validity: {{ txProps.data.txn.firstRound }} -
                        {{ txProps.data.txn.lastRound }} ({{
                          txProps.data.txn.lastRound -
                          txProps.data.txn.firstRound +
                          1
                        }}
                        rounds)
                      </p>
                      <p>Type: {{ txProps.data.type }}</p>
                      <p>Note: {{ formatNote(txProps.data.txn.note) }}</p>
                      <p v-if="txProps.data.txn.group">
                        Group: {{ formatGroup(txProps.data.txn.group) }}
                      </p>
                      <div v-if="txProps.data.type == 'appl'">
                        <p>App Index: {{ txProps.data.txn.appIndex }}</p>
                        <div v-if="txProps.data.txn.appArgs">
                          <p>App Args:</p>
                          <ol>
                            <li
                              v-for="arg in txProps.data.txn.appArgs"
                              :key="arg"
                            >
                              {{ formatAppArg(arg) }}
                            </li>
                          </ol>
                        </div>
                        <div v-if="txProps.data.txn.appAccounts">
                          <p>App Accounts:</p>
                          <ol>
                            <li
                              v-for="acc in txProps.data.txn.appAccounts"
                              :key="acc"
                            >
                              {{ formatAppAccount(acc) }}
                            </li>
                          </ol>
                        </div>
                        <div v-if="txProps.data.txn.appForeignAssets">
                          <p>App Foreign Assets:</p>
                          <ol>
                            <li
                              v-for="asset in txProps.data.txn.appForeignAssets"
                              :key="asset"
                            >
                              {{ asset }}
                            </li>
                          </ol>
                        </div>
                      </div>
                      <div v-if="txProps.data.txn.boxes">
                        <p>Boxes:</p>
                        <ol>
                          <li
                            v-for="box in txProps.data.txn.boxes"
                            :key="box.name"
                          >
                            App Index: {{ box.appIndex }}, Name: {{ box.name }}
                          </li>
                        </ol>
                      </div>
                      <p>Genesis ID: {{ txProps.data.txn.genesisID }}</p>
                      <p>
                        Genesis Hash:
                        {{ formatGenesisHash(txProps.data.txn.genesisHash) }}
                      </p>
                    </div>
                  </template>
                </DataTable>
              </div>
            </template>
          </DataTable>
          <Toast />
        </div>
        <div v-if="error" class="alert alert-danger my-2">
          {{ error }}
        </div>
      </div>
    </div>
  </PublicLayout>
</template>

<script>
import PublicLayout from "../layouts/Public.vue";
import { mapActions } from "vuex";
import algosdk from "algosdk";
import wc from "../shared/wc";

export default {
  components: {
    PublicLayout,
  },
  data() {
    return {
      uri: "",
      auth: "",
      loadingAuth: true,
      addr: "",
      note: "",
      error: "",
      selectedRequest: null,
      selectedTransaction: null,
      expandedRequests: [],
      expandedTransactions: [],
    };
  },
  computed: {
    account() {
      return this.$store.state.wallet.privateAccounts.find(
        (a) => a.addr == this.$route.params.account
      );
    },
    requests() {
      return this.$store.state.wc.requests;
    },
    connectable() {
      return this.addr && this.auth;
    },
  },
  watch: {
    account() {},
  },
  async mounted() {
    this.addr = this.$route.params.account;
    await this.reloadAccount();
    this.prolong();
  },
  methods: {
    ...mapActions({
      accountInformation: "indexer/accountInformation",
      updateAccount: "wallet/updateAccount",
      lastActiveAccount: "wallet/lastActiveAccount",
      deleteAccount: "wallet/deleteAccount",
      setTransaction: "wallet/setTransaction",
      getAsset: "indexer/getAsset",
      prolong: "wallet/prolong",
      setAccountOnline: "kmd/setAccountOnline",
      openSuccess: "toast/openSuccess",
      axiosGet: "axios/get",
      axiosPost: "axios/post",
      getTransactionParams: "algod/getTransactionParams",
      sendRawTransaction: "algod/sendRawTransaction",
      waitForConfirmation: "algod/waitForConfirmation",
    }),
    formatNote(note) {
      try {
        return Buffer.from(note).toString();
      } catch {
        return note;
      }
    },
    formatAppArg(arg) {
      try {
        return Buffer.from(arg).toString("base64");
      } catch {
        return arg;
      }
    },
    formatAppAccount(acc) {
      try {
        return algosdk.encodeAddress(acc.publicKey);
      } catch {
        return acc;
      }
    },
    formatGroup(group) {
      try {
        return group.toString("base64");
      } catch {
        return group;
      }
    },
    formatGenesisHash(genesisHash) {
      return Buffer.from(genesisHash).toString("base64");
    },
    async reloadAccount() {
      try {
        if (this.$route.params.account) {
          await this.accountInformation({
            addr: this.$route.params.account,
          }).then(async (info) => {
            if (info) {
              await this.updateAccount({ info });
              this.auth = info["auth-addr"] ?? this.addr;
            }
          });
        }
      } catch {
        this.auth = this.addr;
      } finally {
        this.loadingAuth = false;
      }
    },
    async clickAccept(id) {
      this.prolong();

      try {
        await wc.acceptRequest(id);

        this.$toast.add({
          severity: "info",
          summary: "Request accepted",
          life: 3000,
        });
      } catch (ex) {
        this.$toast.add({
          severity: "error",
          summary: "Accept request failed",
          detail: ex,
          life: 5000,
        });
      }
    },
    async clickReject(id) {
      this.prolong();

      wc.rejectRequest(id);

      this.$toast.add({
        severity: "info",
        summary: "Request rejected",
        life: 3000,
      });
    },
    async clickDisconnect(id) {
      this.prolong();
      await wc.removeConnector(id);
      this.$toast.add({
        severity: "info",
        summary: "Session removed",
        life: 3000,
      });
    },
    async clickPaste() {
      this.prolong();
      const uri = await navigator.clipboard.readText();
      try {
        await this.clickConnect(uri);
      } catch (ex) {
        this.$toast.add({
          severity: "error",
          summary: "Connect from Clipboard",
          life: 5000,
          detail: ex,
        });
        throw ex;
      }
    },
    async clickConnect(uri) {
      this.prolong();
      wc.createConnector(uri, this.auth, this.addr);

      this.$toast.add({
        severity: "info",
        summary: "Session added",
        life: 3000,
      });
    },
    checkNetwork() {
      if (this.$store.state.config.env == "mainnet-v1.0") {
        return "mainnet";
      }
      if (this.$store.state.config.env == "mainnet") {
        return "mainnet";
      }
      if (this.$store.state.config.env == "testnet-v1.0") {
        return "testnet";
      }
      if (this.$store.state.config.env == "testnet") {
        return "testnet";
      }
      return false;
    },
  },
};
</script>
